// 生成 JSONL 格式的 chunks
const chunks = [];
for (const doc of classics) {
  for (let i = 0; i < doc.chunks.length; i++) {
    chunks.push(JSON.stringify({
      document: {
        source_path: `classics/${doc.title}.md`,
        title: doc.title,
        file_type: "markdown",
        sha256: "",
        byte_size: doc.chunks[i].length,
        extracted_chars: doc.chunks[i].length,
        is_public: true,
      },
      chunk: {
        chunk_index: i,
        content: doc.chunks[i],
      },
    }));
  }
}

// 备份旧文件
const outDir = path.resolve(process.cwd(), "output");
const oldPath = path.join(outDir, "document-chunks.jsonl");
const backupPath = path.join(outDir, "document-chunks-math.jsonl.bak");

if (fs.existsSync(oldPath)) {
  fs.copyFileSync(oldPath, backupPath);
  console.log(`Backed up old chunks to ${backupPath}`);
}

fs.writeFileSync(oldPath, chunks.join("\n") + "\n");
console.log(`Written ${chunks.length} classic chunks to ${oldPath}`);
